const LEFT_DIV = "(";
const RIGHT_DIV = ")";

// =============== Core Text Utilities ===============

// Split a whole raw definition text into logical entries (by numbering like " 2. " or by "word: ").
// - Handles " 1. " normalization
// - Case-insensitive for "word: "
// - Trims and removes empty parts
export function customSplit(word, text) {
  if (!text) return [];

  // Normalize " 1. " to a space (your original behavior)
  text = text.replace(/\s1\.\s/g, " ");

  // Split on " 2. ", " 3. ", ..., " 99. " or on "<word>: " (case-insensitive)
  // We keep the delimiter out using a non-capturing group.
  const wordEsc = escapeRegExp(word);
  const pattern = new RegExp(`(?: [2-9]\\d?\\. |\\b${wordEsc}:\\s)`, "gi");

  return text
    .split(pattern)
    .map((p) => p.trim())
    .filter(Boolean);
}

// Turn a single entry into structured pieces and format them:
// - Extract markers & bracket groups
// - Build a header string (only if a marker exists)
// - Format the remaining definition body
export function tokenizeDefinition(word, entry) {
  const parts = findMarkers(entry);
  if (parts.length === 0) {
    return { header: null, body: "" };
  }

  const hasMarker = parts.some((p) => Array.isArray(p));
  const bodyRaw = parts[parts.length - 1] ?? "";
  const body = formatDefinition(word, bodyRaw);

  if (!hasMarker) {
    // No marker: no header at all (per your rule)
    return { header: null, body };
  }

  // Build header from marker + any bracket groups (before/after)
  const headerPieces = [];
  const marker = parts.find((p) => Array.isArray(p));
  if (marker) headerPieces.push(marker[1]); // human-readable full name

  // Include any bracket groups, but pass them through solveBrackets for abbreviation expansion
  for (const p of parts) {
    if (typeof p === "string" && p.startsWith("(")) {
      headerPieces.push(solveBrackets(word, p));
    }
  }

  const header = headerPieces.join(" ").trim();
  return { header, body };
}

// Group an array of {header, body} into blocks by header.
// header = null → bucket for "no marker"
// Returns an ordered array of { header, bodies[] }
export function groupDefinitionsByHeader(tokenized) {
  const result = [];
  let current = null;

  tokenized.forEach(({ header, body }) => {
    if (header) {
      // Start a new block
      current = { header, bodies: [body] };
      result.push(current);
    } else if (current) {
      // Append to previous block
      current.bodies.push(body);
    } else {
      // No previous header — treat as its own headerless block
      result.push({ header: null, bodies: [body] });
    }
  });

  return result;
}

// =============== Marker Parsing ===============

// Find bracket groups + marker + trailing text.
// Returns array like: ["(…)", [abbr, full], "(…)", "definition text"]
export function findMarkers(definition) {
  if (!definition || !definition.trim()) {
    return [];
  }

  const out = [];
  let remaining = definition.trim();

  // Helper: extract leading "(...)" bracket group(s)
  function takeLeadingBracketGroup() {
    while (remaining.startsWith("(")) {
      const close = remaining.indexOf(")");
      if (close === -1) break;
      const group = remaining.slice(0, close + 1);
      out.push(group);
      remaining = remaining.slice(close + 1).trim();
    }
  }

  takeLeadingBracketGroup();

  // Now see if the first token is an abbreviation marker
  const firstToken = firstWord(remaining);
  if (firstToken && hasOwn(abbreviations, firstToken)) {
    out.push([firstToken, abbreviations[firstToken]]);
    remaining = remaining.slice(firstToken.length).trim();
  }

  // After the marker, more bracket groups may follow
  takeLeadingBracketGroup();

  // Whatever remains is the actual definition text (can be empty)
  if (remaining.length) out.push(remaining);

  return out;
}

// =============== Formatting Helpers ===============

// Expand abbreviations and dashes inside a bracket group "(…)" with support for comma-separated items.
function solveBrackets(word, entry) {
  if (!entry || entry[0] !== "(") return entry;
  const content = entry.slice(1, -1);
  const parts = content.split(",").map((s) => s.trim());
  const expanded = parts.map((p) => formatEntryAtom(word, p));
  return LEFT_DIV + expanded.join(", ") + RIGHT_DIV;
}

// Expand a single bracket atom using abbreviations and dash rules.
function formatEntryAtom(word, entry) {
  const cleaned = entry.endsWith(".") ? entry.slice(0, -1) : entry;

  // 1) direct abbreviation match
  if (hasOwn(abbreviations, entry)) return abbreviations[entry];
  if (hasOwn(abbreviations, cleaned)) return abbreviations[cleaned];

  // 2) dash prefix: "-foo" → either just return or merge with headword
  if (entry[0] === "-") {
    const removedDash = entry.slice(1);
    if (word === removedDash) return entry;

    // find last occurrence of the first letter of removedDash in headword and merge
    const firstLetter = removedDash[0];
    let i = word.length - 1;
    while (i >= 0 && word[i] !== firstLetter) i--;
    if (i >= 0) return word.slice(0, i) + removedDash;
  }

  return entry;
}

// Apply normalization rules to a plain definition line.
// - Case-insensitive patterns based on first letter of headword
// - "x. " at start-of-line
// - " x. " in the middle
// - "x.:" and "x./" anywhere
// - "ld " → "la-mid " anywhere (case-insensitive)
// - remove superscript digits ¹²³⁴⁵⁶⁷⁸⁹⁰
export function formatDefinition(word, definition) {
  if (!definition || !word) return definition;

  const firstLetter = escapeRegExp(word[0]);
  const letterCI = new RegExp(firstLetter, "i");

  // 1) start-of-line "x. "
  definition = definition.replace(
    new RegExp(`^${letterCI.source}\\.\\s`, "i"),
    `${word} `
  );

  // 2) middle " x. "
  definition = definition.replace(
    new RegExp(`\\s${letterCI.source}\\.\\s`, "gi"),
    ` ${word} `
  );

  // 3) "x.:" → "word:"
  definition = definition.replace(
    new RegExp(`\\b${letterCI.source}\\.:`, "gi"),
    `${word}:`
  );

  // 4) "x./" → "word/"
  definition = definition.replace(
    new RegExp(`\\b${letterCI.source}\\.\\/`, "gi"),
    `${word}/`
  );

  // 5) "ld " → "la-mid " (case-insensitive)
  definition = definition.replace(/\bld\s+/gi, "la-mid ");

  // 6) remove superscript digits
  definition = definition.replace(/[¹²³⁴⁵⁶⁷⁸⁹⁰]/g, "");

  // 7) collapse multiple spaces that might arise after replacements
  definition = definition.replace(/\s{2,}/g, " ").trim();

  return definition;
}

// =============== Small Utils ===============
function firstWord(s) {
  if (!s) return "";
  const i = s.indexOf(" ");
  return i === -1 ? s : s.slice(0, i);
}
function hasOwn(obj, key) {
  return Object.prototype.hasOwnProperty.call(obj, key);
}
function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
